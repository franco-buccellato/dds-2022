
    create table Contactos (
        contacto_id bigint generated by default as identity (start with 1),
        apellido varchar(255),
        mail varchar(255),
        nombre varchar(255),
        telefono varchar(255),
        vinculo varchar(255),
        duenio_id bigint,
        primary key (contacto_id)
    )

    create table Duenios (
        id bigint generated by default as identity (start with 1),
        apellido varchar(255),
        fecha_nacimiento date,
        nombre varchar(255),
        numero_identificacion varchar(255),
        tipo_identificacion varchar(255),
        usuario_usuario_id bigint,
        primary key (id)
    )

    create table Rescatistas (
        rescatista_id bigint generated by default as identity (start with 1),
        apellido varchar(255),
        fecha_nacimiento date,
        nombre varchar(255),
        numero_identificacion varchar(255),
        tipo_identificacion varchar(255),
        contacto_id bigint,
        ubicacion_id bigint,
        usuario_id bigint,
        primary key (rescatista_id)
    )

    create table Usuarios (
        usuario_id bigint generated by default as identity (start with 1),
        contrasenia varchar(255),
        nombre_usuario varchar(255),
        tipo_usuario varchar(255),
        primary key (usuario_id)
    )

    create table Voluntarios (
        voluntario_id bigint generated by default as identity (start with 1),
        asociacion_id integer,
        usuario_id bigint,
        primary key (voluntario_id)
    )

    create table asociaciones (
        asociacion_id integer generated by default as identity (start with 1),
        asociacion_nombre varchar(255),
        ubicacion_id bigint,
        primary key (asociacion_id)
    )

    create table caracteristicas (
        tipo_caracteristica varchar(31) not null,
        caracteristica_id integer generated by default as identity (start with 1),
        descripcion varchar(255),
        obligatoria boolean,
        primary key (caracteristica_id)
    )

    create table mascotas (
        mascota_id integer generated by default as identity (start with 1),
        apodo varchar(255),
        descripcion_fisica varchar(255),
        edad_aproximada double,
        nombre varchar(255),
        sexo varchar(255),
        situacion_mascota varchar(255),
        tipo_mascota varchar(255),
        duenio_id bigint,
        primary key (mascota_id)
    )

    create table mascotas_caracteristicas (
        mascota_caracteristica_id bigint generated by default as identity (start with 1),
        respuesta varchar(255),
        caracteristica_id integer,
        mascota_id integer,
        primary key (mascota_caracteristica_id)
    )

    create table mascotas_fotos (
        mascota_id integer not null,
        foto varchar(255) not null
    )

    create table opciones (
        id integer generated by default as identity (start with 1),
        descripcion varchar(255),
        seleccionada boolean,
        opcion_id integer,
        primary key (id)
    )

    create table opciones_nuevas (
        opcion_nueva_id bigint generated by default as identity (start with 1),
        descripcion varchar(255),
        caracteristica_id integer,
        primary key (opcion_nueva_id)
    )

    create table preguntas (
        pregunta_id integer generated by default as identity (start with 1),
        descripcion varchar(255),
        primary key (pregunta_id)
    )

    create table preguntas_adopcion (
        asociacion_id integer not null,
        caracteristica_id integer not null,
        pregunta_id integer not null
    )

    create table publicaciones_adopcion (
        publicacion_adopcion_id integer generated by default as identity (start with 1),
        activa boolean,
        asociacion_asociacion_id integer,
        duenio_id bigint,
        mascota_mascota_id integer,
        primary key (publicacion_adopcion_id)
    )

    create table publicaciones_rescates (
        publicacion_rescate_id bigint generated by default as identity (start with 1),
        publicacion_rescate_estado varchar(255),
        asociacion_id integer,
        rescate_rescate_id bigint,
        primary key (publicacion_rescate_id)
    )

    create table rescates (
        tipo_rescate varchar(31) not null,
        rescate_id bigint generated by default as identity (start with 1),
        rescate_descripcion varchar(255),
        fecha_nacimiento date,
        ubicacion_id bigint,
        mascota_id integer,
        rescatista_id bigint,
        primary key (rescate_id)
    )

    create table ubicaciones (
        ubicacion_id bigint generated by default as identity (start with 1),
        ubicacion_codigo_postal varchar(255),
        ubicacion_direccion varchar(255),
        ubicacion_latitud numeric(19,2),
        ubicacion_localidad varchar(255),
        ubicacion_longitud numeric(19,2),
        primary key (ubicacion_id)
    )

