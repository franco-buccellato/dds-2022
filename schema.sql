
    create table Contactos (
        contacto_id bigint generated by default as identity (start with 1),
        apellido varchar(255),
        mail varchar(255),
        nombre varchar(255),
        telefono varchar(255),
        vinculo varchar(255),
        duenio_id bigint,
        primary key (contacto_id)
    )

    create table Duenios (
        id bigint generated by default as identity (start with 1),
        apellido varchar(255),
        fecha_nacimiento date,
        nombre varchar(255),
        numero_identificacion varchar(255),
        tipo_identificacion varchar(255),
        usuario_usuario_id bigint,
        primary key (id)
    )

    create table Rescatistas (
        rescatista_id bigint generated by default as identity (start with 1),
        apellido varchar(255),
        fecha_nacimiento date,
        nombre varchar(255),
        numero_identificacion varchar(255),
        tipo_identificacion varchar(255),
        contacto_id bigint,
        ubicacion_id bigint,
        usuario_id bigint,
        primary key (rescatista_id)
    )

    create table Usuarios (
        usuario_id bigint generated by default as identity (start with 1),
        contrasenia varchar(255),
        nombre_usuario varchar(255),
        tipo_usuario varchar(255),
        primary key (usuario_id)
    )

    create table Voluntarios (
        voluntario_id bigint generated by default as identity (start with 1),
        asociacion_id integer,
        usuario_id bigint,
        primary key (voluntario_id)
    )

    create table asociaciones (
        asociacion_id integer generated by default as identity (start with 1),
        asociacion_nombre varchar(255),
        ubicacion_id bigint,
        primary key (asociacion_id)
    )

    create table mascotas (
        mascota_id bigint generated by default as identity (start with 1),
        apodo varchar(255),
        descripcion_fisica varchar(255),
        edad_aproximada double,
        nombre varchar(255),
        sexo varchar(255),
        situacion_mascota varchar(255),
        tipo_mascota varchar(255),
        duenio_id bigint,
        primary key (mascota_id)
    )

    create table mascotas_fotos (
        mascota_id bigint not null,
        foto varchar(255) not null
    )

    create table opciones (
        opcion_id bigint generated by default as identity (start with 1),
