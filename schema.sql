    )

    create table publicaciones_rescates (
        publicacion_rescate_id bigint generated by default as identity (start with 1),
        publicacion_rescate_estado varchar(255),
        asociacion_id integer,
        rescate_rescate_id bigint,
        primary key (publicacion_rescate_id)
    )

    create table rescates (
        tipo_rescate varchar(31) not null,
        rescate_id bigint generated by default as identity (start with 1),
        rescate_descripcion varchar(255),
        fecha_nacimiento date,
        ubicacion_id bigint,
        mascota_id integer,
        rescatista_id bigint,
        primary key (rescate_id)
    )

    create table ubicaciones (
        ubicacion_id bigint generated by default as identity (start with 1),
        ubicacion_codigo_postal varchar(255),
        ubicacion_direccion varchar(255),
        ubicacion_latitud numeric,
        ubicacion_localidad varchar(255),
        ubicacion_longitud numeric,
        primary key (ubicacion_id)
    )

